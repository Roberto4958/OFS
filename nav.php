<?php
require_once './Scripts/loginInfo.php';
    $items = '';
    $total_price = 0;
    $total_weight = 0; 
    $total_items = 0;
    $cart = '';
    // Create connection
	$conn = new mysqli($hn, $un, $pw);


    if (!$conn->connect_error){
    	$conn->query("Use OFS");

        $sql = "select i.name, sum(c.amount) as amount, i.weight, i.price from cart c, items i where i.id = c.ItemID and c.userid =1 group by i.id";
        $result = $conn->query($sql);
        $total_items = $result->num_rows;
        
        for($i=0; $i < $total_items; $i++){
        
            $result->data_seek($i);
            $obj = $result->fetch_array(MYSQLI_ASSOC);
            $total_weight += $obj['amount'] * $obj['weight'];
            $total_price += $obj['amount'] * $obj['price'];
            $items .= generateCartItem($obj['name'], $obj['amount'], $obj['weight'], $obj['price']); 
        }
        if($total_items > 0){
            $cart = generateCartHTML($items, $total_price, $total_items);
        }else{
            $cart = generateEmptyCartHTML();
        }
        
        $result->close();
        $conn->close();
        
    } else{
        $cart = generateEmptyCartHTML();
    }

